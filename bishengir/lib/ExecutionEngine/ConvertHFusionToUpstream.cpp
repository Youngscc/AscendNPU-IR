struct ConvertHFusionToUpstream
    : public impl::ExecutionEngineHFusionToUpstreamConversionBase<
          ConvertHFusionToUpstream> {

  using Base::Base;

  void runOnOperation() override {
    auto &ctx = getContext();
    RewritePatternSet patterns(&ctx);

    // Register all HFusion-to-CPU lowering patterns here.
    patterns.add<RewriteHistogramOp>(&ctx);
    patterns.add<RewriteHFusionIsNanOp>(&ctx);
    patterns.add<RewriteHFusionIsFiniteOp>(&ctx);
    patterns.add<RewriteHFusionDeinterleaveOp>(&ctx);
    patterns.add<RewriteHFusionCumprodOp>(&ctx);

    // Future operations like CumprodOp, AtomicXchgOp, etc., should be added below.
    //
    // patterns.add<RewriteXXXXX>(&ctx);

    // Apply all rewrite patterns greedily.
    if (failed(applyPatternsAndFoldGreedily(getOperation(),
                                            std::move(patterns)))) {
      signalPassFailure();
    }
  }
};

